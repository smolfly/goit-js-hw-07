// Напиши скрипт, который для каждого элемента массива ingredients создаст отдельный li,
// после чего вставит все li за одну операцию в список ul.ingredients.Для создания DOM - узлов используй document.createElement().

const ingredients = [
  'Картошка',
  'Грибы',
  'Чеснок',
  'Помидоры',
  'Зелень',
  'Приправы',
];


const listElements = document.querySelector('#ingredients');
const array = [];
ingredients.forEach(element => {
    const itemElement = document.createElement('li');
    itemElement.textContent = element;
    array.push(itemElement);
});
console.log(array);

listElements.append(...array);